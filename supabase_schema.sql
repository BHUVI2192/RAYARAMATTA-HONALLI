ALTER TABLE bookings ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public anonymous insert" ON bookings FOR INSERT WITH CHECK (true);
CREATE POLICY "Admin full access" ON bookings USING (auth.role() = 'service_role');

ALTER TABLE godana_payments ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public anonymous insert" ON godana_payments FOR INSERT WITH CHECK (true);
CREATE POLICY "Admin full access" ON godana_payments USING (auth.role() = 'service_role');
